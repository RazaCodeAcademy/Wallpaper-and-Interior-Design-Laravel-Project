    }

    /**
     * Update the specified resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function update(Request $request, $id)
    {
        $id = 1;
        $categories = Category::find($id);

        $categories->category_title = $request->title;
        $categories->save();
        return redirect('/admin/categories/')->with('success','Data has been updated successfully');
    }

    /**
     * Remove the specified resource from storage.
     *
     * @param  int  $id
     * @return \Illuminate\Http\Response
     */
    public function destroy($id)
    {
        $categories=Category::find($id);
        $categories->delete();
        return redirect('/admin/categories')->with('success','Data has been deleted successfully');
    }
}
