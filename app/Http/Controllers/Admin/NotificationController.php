    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $notifications = new Notification;

        $notifications->name = $request->name;
        $notifications->email = $request->email;
        $notifications->phone = $request->phone;
        $notifications->subject = $request->subject;
        $notifications->messages = $request->message;
        $notifications->save();
        return redirect('/')->with('success','Data has been saved successfully');
    }

    /**
     * Display the specified resource.
